async function runInit() {
  console.log(`\n  ${bold('teletty')} interactive setup`);
  console.log(`  ${dim('We need three things to write your .env file.')}\n`);

  if (fs.existsSync(CWD_ENV)) {
    const overwrite = await ask(`  ${yellow('⚠')}  .env already exists in this directory. Overwrite? [y/N] `);
    if (!/^y/i.test(overwrite)) {
      console.log(`  ${dim('Aborted. Your existing .env was not changed.')}`);
      process.exit(0);
    }
  }

  console.log(`  ${cyan('1/3')}  ${bold('BOT_TOKEN')} — get one from @BotFather:`);
  console.log(`        Open Telegram → search @BotFather → /newbot → choose a name`);
  console.log(`        and a username ending in _bot. Copy the token (looks like`);
  console.log(`        ${dim('123456789:ABCdefGHIjklMNOpqrSTUvwxYZ0123456789')}).\n`);

  const BOT_TOKEN = await askValid(
    `        token: `,
    (v) => /^\d{6,}:[A-Za-z0-9_-]{30,}$/.test(v) ? null : 'Invalid format — expected NUMBERS:LETTERS_DIGITS_DASHES',
    'Tip: copy the entire string BotFather sent you.',
  );

  console.log(`\n  ${cyan('2/3')}  ${bold('Your Telegram user ID')} — get it from @userinfobot:`);
  console.log(`        Open Telegram → search @userinfobot → send any message →`);
  console.log(`        copy the numeric id from its reply.\n`);

  const ALLOWED_USER_IDS = await askValid(
    `        your numeric id: `,
    (v) => /^\d{5,15}$/.test(v) ? null : 'Must be a numeric Telegram user id',
    'Use @userinfobot inside Telegram.',
  );

  console.log(`\n  ${cyan('3/3')}  ${bold('SESSION_SECRET')} — generated for you ${dim('(openssl rand -hex 32 equivalent)')}.`);
  const SESSION_SECRET = crypto.randomBytes(32).toString('hex');

  const envContent = `# Generated by teletty init on ${new Date().toISOString().slice(0, 10)}.
# See https://github.com/olegchetrean/teletty/blob/main/.env.example for every option.

# === Required ===
BOT_TOKEN=${BOT_TOKEN}
ALLOWED_USER_IDS=${ALLOWED_USER_IDS}
SESSION_SECRET=${SESSION_SECRET}

# Production = fail-fast on missing env, stricter logging.
NODE_ENV=production

# === Common optional ===
PORT=7681

# Set this to your public HTTPS origin once you have one (the trycloudflare URL
# from \`teletty tunnel\`, or your own domain). Empty = accept any origin (DEV ONLY).
ALLOWED_ORIGINS=

# Working directory for shell sessions. Default: $HOME.
SHELL_CWD=
`;

  fs.writeFileSync(CWD_ENV, envContent, { mode: 0o600 });
  console.log(`\n  ${green('✓')}  Wrote ${cyan(CWD_ENV)} ${dim('(mode 600 — owner-only)')}.`);
  console.log(`\n  ${bold('Next:')}`);
  console.log(`    ${cyan('teletty')}                ${dim('# start the server')}`);
  console.log(`    ${cyan('teletty tunnel')}         ${dim('# in another terminal — public HTTPS URL')}`);
  console.log(`    Then paste the URL into @BotFather → Menu Button. ${dim('(see README)')}\n`);
}

function runTunnel() {
  // Check cloudflared is installed
  const which = spawnSync('command', ['-v', 'cloudflared'], { shell: true });
  if (which.status !== 0) {
    console.log(`\n  ${red('✖')}  ${bold('cloudflared')} is not installed.\n`);
    console.log(`  Install it:`);
    console.log(`    ${cyan('Linux:')}   curl -sL https://github.com/cloudflare/cloudflared/releases/latest/download/cloudflared-linux-amd64 -o /usr/local/bin/cloudflared && sudo chmod +x /usr/local/bin/cloudflared`);
    console.log(`    ${cyan('macOS:')}   brew install cloudflared`);
    console.log(`    ${cyan('Windows:')} winget install --id Cloudflare.cloudflared\n`);
    process.exit(1);
  }

  const port = process.env.PORT || '7681';
  console.log(`\n  ${bold('teletty tunnel')} ${dim(`(exposing http://127.0.0.1:${port})`)}\n`);

  const child = spawn('cloudflared', ['tunnel', '--url', `http://127.0.0.1:${port}`, '--no-autoupdate'], {
    stdio: ['ignore', 'pipe', 'pipe'],
  });

  let urlPrinted = false;
  const onChunk = (buf) => {
    const s = buf.toString();
    process.stderr.write(dim(s));
    const m = s.match(/https:\/\/[a-z0-9-]+\.trycloudflare\.com/);
    if (m && !urlPrinted) {
      urlPrinted = true;
      const url = m[0];
      console.log(`\n  ${green('✓')}  Tunnel up: ${bold(cyan(url))}\n`);
      console.log(`  ${bold('Now in Telegram:')}`);
      console.log(`    1. Open @BotFather → /mybots → your bot`);
      console.log(`    2. Bot Settings → Menu Button → Configure Menu Button`);
      console.log(`    3. Paste URL: ${cyan(url)}`);
      console.log(`    4. Button text: ${cyan('Terminal')}\n`);
      console.log(`  ${dim('Then open your bot in Telegram and tap the Terminal button.')}`);
      console.log(`  ${dim('Tip: this URL changes on every restart. For a stable URL,')}`);
      console.log(`  ${dim('use a named Cloudflare tunnel or your own domain (see README).')}\n`);
      console.log(`  ${dim('Press Ctrl+C to stop.')}`);
    }
  };
  child.stdout.on('data', onChunk);
  child.stderr.on('data', onChunk);
  child.on('exit', (code) => process.exit(code || 0));
  process.on('SIGINT', () => child.kill('SIGINT'));
  process.on('SIGTERM', () => child.kill('SIGTERM'));
}

function runDoctor() {
  console.log(`\n  ${bold('teletty doctor')} ${dim(`v${PKG.version}`)}\n`);
  let problems = 0;
  let warnings = 0;

  const ok = (msg) => console.log(`  ${green('✓')} ${msg}`);
  const fail = (msg, hint) => { problems++; console.log(`  ${red('✖')} ${msg}${hint ? `\n      ${dim(hint)}` : ''}`); };
  const warn = (msg, hint) => { warnings++; console.log(`  ${yellow('!')} ${msg}${hint ? `\n      ${dim(hint)}` : ''}`); };

  // Node version
  const major = parseInt(process.versions.node.split('.')[0], 10);
  if (major >= 20) ok(`Node.js ${process.versions.node}`);
  else fail(`Node.js ${process.versions.node} is too old`, 'Need Node.js 20 or newer.');

  // .env file
  if (!fs.existsSync(CWD_ENV)) {
    fail(`No .env in ${process.cwd()}`, 'Run `teletty init` first.');
    console.log(`\n  ${red(`${problems} problem${problems > 1 ? 's' : ''}, ${warnings} warning${warnings === 1 ? '' : 's'}`)}\n`);
    process.exit(1);
  }
  ok(`.env present (${dim(CWD_ENV)})`);

  // Read .env
  const env = {};
  fs.readFileSync(CWD_ENV, 'utf8').split('\n').forEach((line) => {
    const m = line.match(/^\s*([A-Z_]+)\s*=\s*(.*)\s*$/);
    if (m) env[m[1]] = m[2].replace(/^['"]|['"]$/g, '');
  });

  // BOT_TOKEN
  if (!env.BOT_TOKEN) fail('BOT_TOKEN missing', 'Get one from @BotFather: /newbot');
  else if (!/^\d{6,}:[A-Za-z0-9_-]{30,}$/.test(env.BOT_TOKEN)) fail('BOT_TOKEN format looks wrong', 'Expected NUMBERS:LETTERS_DIGITS_DASHES, like 123456789:ABCdef…');
  else ok(`BOT_TOKEN present (${dim(env.BOT_TOKEN.slice(0, 8) + '…')})`);

  // ALLOWED_USER_IDS
  if (!env.ALLOWED_USER_IDS) fail('ALLOWED_USER_IDS missing', 'Find your id with @userinfobot.');
  else if (!/^\d+(,\d+)*$/.test(env.ALLOWED_USER_IDS.replace(/\s/g, ''))) fail('ALLOWED_USER_IDS must be comma-separated numeric ids');
  else ok(`ALLOWED_USER_IDS: ${env.ALLOWED_USER_IDS}`);

  // SESSION_SECRET
  if (!env.SESSION_SECRET) fail('SESSION_SECRET missing', 'Generate one: openssl rand -hex 32');
  else if (env.SESSION_SECRET.length < 32) warn('SESSION_SECRET is shorter than 32 hex chars', 'Recommend `openssl rand -hex 32` (64 hex chars).');
  else ok('SESSION_SECRET present and long enough');

  // ALLOWED_ORIGINS in production
  if ((env.NODE_ENV || '') === 'production' && !env.ALLOWED_ORIGINS) {
    warn('ALLOWED_ORIGINS empty in production', 'Set it to your public HTTPS URL (e.g. the trycloudflare URL).');
  } else if (env.ALLOWED_ORIGINS) {
    ok(`ALLOWED_ORIGINS: ${env.ALLOWED_ORIGINS}`);
  }

  // Risky toggles
  if (env.MGMT_TOKEN) warn('MGMT_TOKEN is set — /api/exec is enabled (RCE-as-a-feature)', 'Leave it empty unless you really need the emergency exec endpoint.');
  if (env.AUDIT_LOG_DIR) warn(`AUDIT_LOG_DIR=${env.AUDIT_LOG_DIR}`, 'Captures every keystroke + reply, including secrets you paste.');

  // tmux
  const tmux = spawnSync('tmux', ['-V'], { stdio: 'pipe' });
  if (tmux.status === 0) ok(`tmux ${tmux.stdout.toString().trim()}`);
  else fail('tmux not installed', 'Install with `apt install tmux` / `brew install tmux`.');

  // cloudflared (advisory)
  const cf = spawnSync('cloudflared', ['--version'], { stdio: 'pipe' });
  if (cf.status === 0) ok(`cloudflared ${cf.stdout.toString().trim().slice(0, 60)}`);
  else warn('cloudflared not installed', 'Optional, only needed for `teletty tunnel`.');

  // Port free?
  const port = parseInt(env.PORT || '7681', 10);
  const net = require('net');
  const probe = net.createServer();
  probe.once('error', (err) => {
    if (err.code === 'EADDRINUSE') warn(`Port ${port} is already in use`, 'Stop the other process or change PORT in .env.');
    else warn(`Could not probe port ${port}: ${err.message}`);
    finish();
  });
  probe.once('listening', () => { ok(`Port ${port} is free`); probe.close(finish); });
  probe.listen(port, '127.0.0.1');

  function finish() {
    console.log();
    if (problems === 0 && warnings === 0) console.log(`  ${green(bold('All checks passed.'))} Run \`teletty\` to start.\n`);
    else if (problems === 0) console.log(`  ${yellow(`${warnings} warning${warnings === 1 ? '' : 's'}`)} — fix when convenient.\n`);
    else console.log(`  ${red(`${problems} problem${problems > 1 ? 's' : ''}, ${warnings} warning${warnings === 1 ? '' : 's'}`)} — fix the problems first.\n`);
    process.exit(problems > 0 ? 1 : 0);
  }
}

// ─── Dispatch ────────────────────────────────────────────────────────────────
if (cmd === '--help' || cmd === '-h' || cmd === 'help') { showHelp(); process.exit(0); }
if (cmd === '--version' || cmd === '-V' || cmd === 'version') { console.log(PKG.version); process.exit(0); }
if (cmd === 'init') { runInit().catch((e) => { console.error(e); process.exit(1); }); }
else if (cmd === 'tunnel') { runTunnel(); }
else if (cmd === 'doctor') { runDoctor(); }
else if (!cmd || cmd === 'start') {
  // Default: start the server
  process.chdir(process.cwd()); // make sure CWD is preserved
  // Load .env from current working dir by passing it to dotenv via env var
  process.env.DOTENV_CONFIG_PATH = CWD_ENV;
  require(path.join(ROOT, 'server.js'));
}
else { console.error(`Unknown command: ${cmd}\nRun \`teletty --help\``); process.exit(1); }
