#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const crypto = require('crypto');
const readline = require('readline');
const { spawn, spawnSync } = require('child_process');

const PKG = JSON.parse(fs.readFileSync(path.join(__dirname, '..', 'package.json'), 'utf8'));
const ROOT = path.join(__dirname, '..');
const CWD_ENV = path.join(process.cwd(), '.env');

const args = process.argv.slice(2);
const cmd = args[0];

function ansi(code, s) { return process.stdout.isTTY ? `\x1b[${code}m${s}\x1b[0m` : s; }
const dim = (s) => ansi('2', s);
const bold = (s) => ansi('1', s);
const green = (s) => ansi('32', s);
const red = (s) => ansi('31', s);
const yellow = (s) => ansi('33', s);
const cyan = (s) => ansi('36', s);

async function ask(prompt, { mask = false, validate = null } = {}) {
  const rl = readline.createInterface({ input: process.stdin, output: process.stdout, terminal: true });
  if (mask && process.stdin.isTTY) {
    // Hide echo for sensitive values
    process.stdout.write(prompt);
    return new Promise((resolve) => {
      let answer = '';
      const onData = (ch) => {
        const s = ch.toString();
        if (s === '\r' || s === '\n') {
          process.stdin.removeListener('data', onData);
          process.stdin.setRawMode(false);
          process.stdin.pause();
          process.stdout.write('\n');
          rl.close();
          resolve(answer.trim());
        } else if (s === '\x03') { // Ctrl+C
          process.stdin.removeListener('data', onData);
          process.stdin.setRawMode(false);
          process.stdin.pause();
          process.stdout.write('\n');
          rl.close();
          process.exit(130);
        } else if (s === '\x7f' || s === '\b') { // backspace / delete
          if (answer.length) { answer = answer.slice(0, -1); process.stdout.write('\b \b'); }
        } else if (s.charCodeAt(0) >= 32) { // printable only
          answer += s;
          process.stdout.write('*');
        }
      };
      process.stdin.setRawMode(true);
      process.stdin.resume();
      process.stdin.on('data', onData);
    });
  }
  return new Promise((resolve) => {
    rl.question(prompt, (val) => {
      rl.close();
      resolve((val || '').trim());
    });
  });
}

async function askValid(prompt, validate, hint) {
  while (true) {
    const v = await ask(prompt);
    const err = validate(v);
    if (!err) return v;
    console.log(`  ${red('✖')} ${err}${hint ? `\n  ${dim(hint)}` : ''}`);
  }
}

